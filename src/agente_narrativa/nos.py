    mensagens = [
        SystemMessage(content=PROMPT_SISTEMA),
        HumanMessage(content=_montar_prompt_usuario(e)),
    ]
    resposta = llm.invoke(mensagens)
    return {
        "entrada": e,
        "historia": resposta.content,
        "historico": mensagens + [AIMessage(content=resposta.content)],
        "feedback": "",
    }


